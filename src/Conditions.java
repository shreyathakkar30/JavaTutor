import java.util.*;

public class Conditions {
    public static void main(String[] args){
        //Lecture 3: If else Condition
        Scanner sc = new Scanner(System.in);
        int age = sc.nextInt();

        if(age > 18){
            System.out.println("Adult");
        }else{
            System.out.println("Not Adult");
        }



    }
}
